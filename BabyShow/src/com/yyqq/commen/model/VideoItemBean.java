package com.yyqq.commen.model;

public class VideoItemBean {

	int no;
	String title;
	String img;
	String url;
	public boolean isVideoShow;

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public boolean isVideoShow() {
		return isVideoShow;
	}

	public void setVideoShow(boolean isVideoShow) {
		this.isVideoShow = isVideoShow;
	}

	@Override
	public String toString() {
		return "VideoItemBean [no=" + no + ", title=" + title + ", img=" + img
				+ ", url=" + url + ", isVideoShow=" + isVideoShow + "]";
	}

}
