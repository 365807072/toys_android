package com.yyqq.commen.model;

/**
 * 押金使用详情
 * 
 * */
public class ToysLeaseDepositDetailBean {

	private String year = "";
	private String hour = "";
	private String monery = "";
	private String detail = "";
	private String post_create_time = "";

	public ToysLeaseDepositDetailBean() {
	}

	public ToysLeaseDepositDetailBean(String year, String hour, String monery,
			String detail) {
		super();
		this.year = year;
		this.hour = hour;
		this.monery = monery;
		this.detail = detail;
	}

	public ToysLeaseDepositDetailBean(String year, String hour, String monery,
			String detail, String post_create_time) {
		super();
		this.year = year;
		this.hour = hour;
		this.monery = monery;
		this.detail = detail;
		this.post_create_time = post_create_time;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getHour() {
		return hour;
	}

	public void setHour(String hour) {
		this.hour = hour;
	}

	public String getMonery() {
		return monery;
	}

	public void setMonery(String monery) {
		this.monery = monery;
	}

	public String getDetail() {
		return detail;
	}

	public void setDetail(String detail) {
		this.detail = detail;
	}

	public String getPost_create_time() {
		return post_create_time;
	}

	public void setPost_create_time(String post_create_time) {
		this.post_create_time = post_create_time;
	}

	@Override
	public String toString() {
		return "ToysLeaseDepositDetailBean [year=" + year + ", hour=" + hour
				+ ", monery=" + monery + ", detail=" + detail
				+ ", post_create_time=" + post_create_time + "]";
	}

}
