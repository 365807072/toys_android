package com.yyqq.commen.model;

/**
 * 订单明细可选项
 * */
public class OrderDetailedOptionBean {

	private String search_id;
	private String search_time;
	private String order_num;
	private String monery;

	public OrderDetailedOptionBean() {
	}

	public OrderDetailedOptionBean(String search_id, String search_time,
			String order_num, String monery) {
		super();
		this.search_id = search_id;
		this.search_time = search_time;
		this.order_num = order_num;
		this.monery = monery;
	}

	public String getSearch_id() {
		return search_id;
	}

	public void setSearch_id(String search_id) {
		this.search_id = search_id;
	}

	public String getSearch_time() {
		return search_time;
	}

	public void setSearch_time(String search_time) {
		this.search_time = search_time;
	}

	public String getOrder_num() {
		return order_num;
	}

	public void setOrder_num(String order_num) {
		this.order_num = order_num;
	}

	public String getMonery() {
		return monery;
	}

	public void setMonery(String monery) {
		this.monery = monery;
	}

	@Override
	public String toString() {
		return "OrderDetailedOptionBean [search_id=" + search_id
				+ ", search_time=" + search_time + ", order_num=" + order_num
				+ ", monery=" + monery + "]";
	}

}
