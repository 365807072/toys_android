package com.yyqq.commen.model;

public class ToysLeaseBatteryBean {

	private String order_id = "";
	private String battery_title = "";
	private String is_battery = "";

	public ToysLeaseBatteryBean() {
	}

	public ToysLeaseBatteryBean(String order_id, String battery_title,
			String is_battery) {
		super();
		this.order_id = order_id;
		this.battery_title = battery_title;
		this.is_battery = is_battery;
	}

	public String getOrder_id() {
		return order_id;
	}

	public void setOrder_id(String order_id) {
		this.order_id = order_id;
	}

	public String getBattery_title() {
		return battery_title;
	}

	public void setBattery_title(String battery_title) {
		this.battery_title = battery_title;
	}

	public String getIs_battery() {
		return is_battery;
	}

	public void setIs_battery(String is_battery) {
		this.is_battery = is_battery;
	}

	@Override
	public String toString() {
		return "ToysLeaseBatteryBean [order_id=" + order_id
				+ ", battery_title=" + battery_title + ", is_battery="
				+ is_battery + "]";
	}

}
