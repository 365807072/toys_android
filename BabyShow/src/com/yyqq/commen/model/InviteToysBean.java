package com.yyqq.commen.model;

/**
 * 邀请好友-玩具列表
 * 
 * */
public class InviteToysBean {

	private String toysIcon = "";

	public InviteToysBean() {
	}

	public InviteToysBean(String toysIcon) {
		super();
		this.toysIcon = toysIcon;
	}

	public String getToysIcon() {
		return toysIcon;
	}

	public void setToysIcon(String toysIcon) {
		this.toysIcon = toysIcon;
	}

	@Override
	public String toString() {
		return "InviteToysBean [toysIcon=" + toysIcon + "]";
	}

}
