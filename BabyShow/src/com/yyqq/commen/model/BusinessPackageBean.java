package com.yyqq.commen.model;

/**
 * 商家详情的套餐列表Bean
 * */
public class BusinessPackageBean {

	private String packageId; // 套餐Id
	private String presentPrice; // 现价
	private String originalPrice; // 原价
	private String packageCotent; // 套餐内容
	private String packagePrice; // 原项目单价
	private String frequency; // 可玩次数
	private String time; // 单次时间
	private String validityPeriod; // 有效期
	private String isBuy; // 是否免费或者首次体验过
	private String packageName; // 套餐名称
	private String business_member_price = ""; // 玩具租赁会员价

	public BusinessPackageBean() {
	}

	public BusinessPackageBean(String packageId, String presentPrice,
			String originalPrice, String packageCotent, String packagePrice,
			String frequency, String time, String validityPeriod, String isBuy,
			String packageName, String business_member_price) {
		super();
		this.packageId = packageId;
		this.presentPrice = presentPrice;
		this.originalPrice = originalPrice;
		this.packageCotent = packageCotent;
		this.packagePrice = packagePrice;
		this.frequency = frequency;
		this.time = time;
		this.validityPeriod = validityPeriod;
		this.isBuy = isBuy;
		this.packageName = packageName;
		this.business_member_price = business_member_price;
	}

	public String getPackageId() {
		return packageId;
	}

	public void setPackageId(String packageId) {
		this.packageId = packageId;
	}

	public String getPresentPrice() {
		return presentPrice;
	}

	public void setPresentPrice(String presentPrice) {
		this.presentPrice = presentPrice;
	}

	public String getOriginalPrice() {
		return originalPrice;
	}

	public void setOriginalPrice(String originalPrice) {
		this.originalPrice = originalPrice;
	}

	public String getPackageCotent() {
		return packageCotent;
	}

	public void setPackageCotent(String packageCotent) {
		this.packageCotent = packageCotent;
	}

	public String getPackagePrice() {
		return packagePrice;
	}

	public void setPackagePrice(String packagePrice) {
		this.packagePrice = packagePrice;
	}

	public String getFrequency() {
		return frequency;
	}

	public void setFrequency(String frequency) {
		this.frequency = frequency;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getValidityPeriod() {
		return validityPeriod;
	}

	public void setValidityPeriod(String validityPeriod) {
		this.validityPeriod = validityPeriod;
	}

	public String getIsBuy() {
		return isBuy;
	}

	public void setIsBuy(String isBuy) {
		this.isBuy = isBuy;
	}

	public String getPackageName() {
		return packageName;
	}

	public void setPackageName(String packageName) {
		this.packageName = packageName;
	}

	public String getBusiness_member_price() {
		return business_member_price;
	}

	public void setBusiness_member_price(String business_member_price) {
		this.business_member_price = business_member_price;
	}

	@Override
	public String toString() {
		return "BusinessPackageBean [packageId=" + packageId
				+ ", presentPrice=" + presentPrice + ", originalPrice="
				+ originalPrice + ", packageCotent=" + packageCotent
				+ ", packagePrice=" + packagePrice + ", frequency=" + frequency
				+ ", time=" + time + ", validityPeriod=" + validityPeriod
				+ ", isBuy=" + isBuy + ", packageName=" + packageName
				+ ", business_member_price=" + business_member_price + "]";
	}

}
